namespace ctopp {
struct NetworkModel::Impl {
    void*       skel    = nullptr;  // struct traffic_monitor_bpf *
    void*       ringbuf = nullptr;  // struct ring_buffer *
    std::string bpf_obj_path;

    // TC attach state (for clean detach during stop)
    uint32_t ingress_handle   = 0;
    uint32_t ingress_priority = 0;
    bool     ingress_attached = false;
    uint32_t egress_handle    = 0;
    uint32_t egress_priority  = 0;
    bool     egress_attached  = false;
};
} // namespace ctopp

